// Helper to get all words from selected categories
export function getWordsForCategories(categoryIds: string[]): string[] {
  const allWords: string[] = [];

  for (const categoryId of categoryIds) {
    if (categoryId in wordSets) {
      allWords.push(...wordSets[categoryId as keyof typeof wordSets]);
    }
    else if (categoryId in premiumWordSets) {
      // In MVP, premium words are available (no auth required yet)
      allWords.push(...premiumWordSets[categoryId as keyof typeof premiumWordSets]);
    }
  }

  return allWords;
}

// Get random word from selected categories
export function getRandomWord(categoryIds: string[]): string {
  const words = getWordsForCategories(categoryIds);
  if (words.length === 0) {
    throw new Error('No words available for selected categories');
  }
  return words[Math.floor(Math.random() * words.length)];
}
